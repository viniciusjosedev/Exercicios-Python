'''print ("----VARIÁVEIS----")
Variaveis nada mais é do que um local aonde se guarda alguma informação
Seja ela do tipo Texto (String), Numeros inteiros (int) ou "quebrados" (float)
ou seja, variaveis guarda algum tipo de valor e saber manipular estes valores é muito importante na decorrencia do 
código'''

'''print ("----Váriaveis II----")
A estrutura padrão de uma variável é a seguinte:
- Um Nome (este nome vai ser o que guardara o valor da variável, a todo o momento o mesmo fica no lado esquerdo
da estrutura)
- um valor (O valor irá passar a informação para dentro da variavel, sempre ficando no lado direito depois da igualdade)
- um tipo (Toda Variável ten-se um tipo, por exemplo, se no valor eu colocar uma informação númerica inteira,
o tipo da minha variavel vai ser inteiro (int), já se eu colocar texto, sempre intruduzido pelas (""), vai ser 
do tipo string e assim por diante)
- um tamanho (dependendo do valor que contem a variavel (nome) o tamanho pode alterar, podendo chegar a GB, e o limite
nada mais é do que o seu HardWare)'''

'''print ("----Manipulação de Dados----")
A partir do Momento em que declaramos um valor a uma determinada variável o proprio python irá determinar o tipo
da mesma, ou seja, de declararmos uma variavel com nome a, e darmos o valor 5  á ela, automaticamente a mesma
será do tipo inteiro, o mesmo acontece de declararmos o valor 7.2, assim, será do tipo float, e assim por diante.
Ou seja, não necessitamos de colocar o tipo de variavle esplicito.'''

